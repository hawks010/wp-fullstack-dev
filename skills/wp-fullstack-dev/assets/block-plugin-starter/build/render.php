<?php
/**
 * Dynamic block rendering template.
 *
 * @package ExampleDynamicBlock
 * @author  Sonny x Inkfire
 *
 * @var array<string, mixed> $attributes Block attributes.
 */

declare(strict_types=1);

$message = wp_kses_post( (string) ( $attributes['content'] ?? '' ) );
$label   = sanitize_text_field( (string) ( $attributes['label'] ?? '' ) );
$wrapper = get_block_wrapper_attributes(
	array(
		'class'      => 'example-dynamic-message',
		'aria-label' => $label,
	)
);
?>
<div <?php echo $wrapper; // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Generated by core. ?>>
	<p><?php echo $message; // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Sanitized with wp_kses_post(). ?></p>
</div>
